		<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left">
		<img src="photos/culture_con.png" class="img-responsive" style="padding-right:20px; padding-bottom:20px;" align="left">
			Unlike many of the other great mountains of the world which have been treated in popular lore as the homes of demons and evil spirits, the Himalaya and Himachal have always been considered as benign and life-giving. These are places of solace and sanctuary. With just the woods and snow and icy winds for company, to these heights retreated the great sages of yore and their wisdom gave India the spine of much of its identity. <br>
<br>
Culturally and geographically, the state has three fairly well-defined zones. The 'tribal belt' that holds the districts of Kinnaur and Lahaul-Spiti is largely Buddhist and the language belongs to the Himalayan belt of Tibeto- Burmese. <br>
<br>
The mid-belt hugs this close and is characterised by forested hills and cultivated valleys — with hamlets, farms and orchards interspersed over the slopes. <br>
<br>
Himachal’s sub-montane dwellers practise settled cultivation and this is the area that has traditionally held the greatest concentration of population.<br>

</p>
<span style="font-family:'Fengardo Neue'; font-size:30px; color:#1db9f3">Religion</span><br>

<hr>
<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left">
In Himachal, the freshest of leaves are nurtured by roots that go centuries deep – and the nourishment has come from scores of different strands. 

South of the Greater Himalaya, the presence of Hinduism is strong. In the mid-hills, pastoral presences appear in the worship or numerous local ‘devtas’ and ‘devis’. 

In the Trans Himalaya, Buddhism has thrived for over a thousand years. 

The presence of Christianity came with the arrival of the British and the state has over a dozen churches spread over its area. 

Similarly, there are several places across the state that are held sacred by the Sikhs. 

Islam registers its presence in and around Nahan and in some of the larger towns.
</p>
<br>

<span style="font-family:'Fengardo Neue'; font-size:30px; color:#1db9f3">Fairs and Festivals </span><br>

<hr>
<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left">
<img src="photos/cultureFairs-&amp;-Fests.png" class="img-responsive"  style="padding-right:20px; padding-bottom:20px;" align="left">
Most of the fairs and festivals of Himachal are a celebration of life, or have religious or agrarian roots. Practically every major festival of North India is celebrated in Himachal — and each one has its own special touch. <br>
<br>
In addition, there are some two thousand deities worshipped in Himachal – and numerous fairs and festivals are held in their honour. There are others that began as if yesterday and have added their colours to a grand collage. From seasons to sports and from religion to trade this is a joyous celebration of life. <br>
<br>
Practically without exception, every village has its little fair or festival. There are some which are small family or community affairs and there are others where people by the thousand may be present. Some are very unusual, like the Phulaich/Flaich or Ookhayang in Kinnaur; this among other things commemorates the end of summer and the onset of winter. Every village sends out its members to collect flowers from the hillsides and these are then gathered in the village square. It is a time of festivities and traditional dances. Other festivals are marked with buffalo fights and wrestling matches. Almost all have dances, music and folk-singing. <br>
<br>
One of the most spectacular festivals, with nuances that are special to Himachal is the Dussehra celebration in Kullu in the month of October. This commemorates the victory of Lord Rama over the demon king, Ravana — an event that has come to symbolise the triumph of good over evil in Indian tradition. On Kullu's open Dhalpur Maidan, the chariot of Raghunath Ji (as Bhagwan Ram is known in the valley), is wheeled out of the temple and the celebrations begin when the image of the goddess Hadimba Devi arrives from neighbouring Manali. Some two hundred deities from all over the area also gather to pay tribute to Raghunath Ji.<br>
<br>
<span style="font-family:'Fengardo Neue'; font-size:30px; color:#1db9f3">Dance and music</span><br>

<hr>
<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left">
 <img src="photos/culture_dance.png" class="img-responsive" style="padding-right:20px; padding-bottom:20px;" align="left">
Himachal Pradesh is a land of festivals and dances that form an inherent part of its culture. Dances are notably performed at festivals and other special occasions like weddings, lending color and variety to everyday monotony. The varied forms and styles of dances reflect the true spirit of the people. Through beautiful folk dances and honey sweet pahari songs, they express merriment and exhibit a close bond with their roots. <br>
<br>
The most popular dances of the state are Rakshasa (demon), Kayang, Bakayang, the Bnayangchu, the Jataru Kayang, Chohara, Shand and Shabu, Lang-dar-ma, Nati, Jhanjhar, Jhoor, Gi and Rasa. A myriad of musical instruments like Ranasingha, Karna, Turhi, Flute, Ektara, Kindari, Jhanjh, Manjara, Chimta, Ghariyal and Ghunghru are played to provide music for the songs and the dances. <br>
<br>
Chham dance is one of the most colourful and splendid dance forms of Himachal. It is performed by a sect of Buddhists — usually monks and lamas in monasteries on special occasions and festivities. Dancers dress up in monstrous appearances and embody the evil spirits that are supposed to bring natural disasters and disease among mankind. According to legend, the dance form is pious and performing it is known to ward off evils and spirits. <br>
<br>
Kullu Nati is another famous dance of the state that is performed with much fanfare. Dancers dressed in ethnic attires groove to the beats of several musical instruments like dhol, nagara, narsimha etc. People gyrate their bodies in slow swaying movements; they dance either by making circles or by standing in rows. This dance form is performed during the New Year and celebrates the new harvest ready for reaping.<br>
<br>
Thoda from the hinterlands of Himachal Pradesh is a dance form that derives origins from martial arts. Also known as the dance of archery, Thoda is performed by two teams, descendants of Kuaravas and Pandavas respectively. The two opposing parties make continuous attempts to attack each other and defend themselves at the same time. They use arrows and bows swiftly and skillfully to create an illusion of real battle.<br>
<br>
</p>

<span style="font-family:'Fengardo Neue'; font-size:30px; color:#1db9f3">Handicrafts</span><br>
<hr>
<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left">
Himachal's diverse cultural and historical influences have produced a variety of handicrafts and arts. Some were created for household use – and then there were a few court crafts, like the fine miniature paintings and the Chamba rumal, the handkerchief.<br>
<br>
<img src="photos/culture_handicraft.jpg" class="img-responsive" style="padding-right:20px; padding-bottom:20px;" align="left">
There are fine skills in the spinning and weaving of wool — the delicacy of the pashmina shawl and the thicker, more functional, brightly patterned woollen shawls. The vivid shawls of Kangra, Kullu and Kinnaur are famed worId over. The local tweeds, pattu are rough, rustic and unbelievable warm. The need to keep the chill at bay has given rise to warm and wonderfully embroidered footwear and the distinctive caps of Himachal. The paiche of Kinnaur and the kapul of Lahaul and Spiti keep feet as warm as the proverbial toast. The pullas of the Kullu are perfect carpet slippers. The range of carpets has gained richly from the ties with Tibet. There is a boldness of design and their use was once as varied as saddle drapes curtains, bed-spreads and wall coverings. The jewellery is ornate to say the least. The women of Himachal are often adorned by such a mass of exquisitely worked silver that their faces are barely visible. An unusual item of Himachal's metal ware lies in the moharas or busts of deities and village or household gods. Then there is a range of statuettes, vessels, bells in brass or copper - and occasionally, silver. <br>
<br>
Wood makes its presence felt in carvings, walking sticks, furniture and bowls. Bamboo and grass mats, leather products and the little dolls of Himachal are both souvenirs and utility items.<br>
<br>
</p>

<span style="font-family:'Fengardo Neue'; font-size:30px; color:#1db9f3">Cuisine</span><br>

<hr>
<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left">
Himachal Pradesh offers a range of culinary delights to tourists. There is a wide range of lip smacking and savoury food items available to choose from. The cuisine of Himachal Pradesh is influenced by Punjabi and Tibetan styles of cooking. Himachalis are known for their simple lifestyles and are very hardworking. In their daily and regular routine most of them prefer simple dishes that are easy to make and tasty to eat. <br>
<br>
One of the most important food items of the people of Himachal is dal or lentil and chawal or plain rice. The simple preparation of rice and pulses provides the daily intake of carbohydrates and proteins, the two important components that help in building a healthy body. Roti or flat bread with any vegetable is also one of the staple food items of the people of this hilly state. <br>
<br>
Cooked with wheat flour and savoured with ghee or butter, Sidu is a well known dish of the state. Sidu is generally made at weddings or other festive occasions and consumed mostly in the winter months. It is made both with a savoury or a sweet filling. Patande is a well known dish from the district of Sirmour. <br>
<br>
Made from wheat, Patande is like a pancake, popularly known as the Indian Pancake of Himachal Pradesh. Meat is commonly consumed throughout the state. It is generally cooked with whole spices like cardamom, cinnamon, cloves, red chillies and coriander leaves. The aromatic meat is very appetizing and a source of energy in the cold weather of the state. <br>
<br>
Himachal Pradesh celebrates the Food festival of ‘Dham’, which attracts several tourists from far and wide. There is a wide variety of food items reflecting the vibrant, dynamic and simple life of the people of Himachal Pradesh.<br>
