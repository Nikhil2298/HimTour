<img src="photos/kangra.jpeg" style="padding-right:20px; padding-bottom:20px;" class="img-responsive" align="left">
 <p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left">About 2 million years ago man lived in the foothills of Himachal Pradesh, viz in the Bangana valley of Kangra, Sirsa valley of Nalagarh and Markanda valley of Sirmour. The foothills of the state were inhabited by people from Indus valley civilization which flourished between 2250 and 1750 B.C. People of Indus valley civilization pushed the original inhabitants of Ganga plains who were known as Kolorian people towards north. They moved to the hills of Himachal Pradesh where they could live peacefully and preserve their way of life.</p><br>


 <p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left">In the Vedas they have been referred to as Dasas, Dasyus and Nishadas while in later works they have been called Kinnars, Nagas and Yakshas. The Kols or Mundas are believed to be the original migrants to the hills of present day Himachal.</p><br>


 <p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left">The second phase of migrants came in the form of Mongoloid people known as Bhotas and Kiratas. Later on came the third and most important wave of migrants in the form of the Aryans who left their Central Asian home. These laid the base of history and culture of Himachal Pradesh.</p><br>


<span style="font-family:'Fengardo Neue'; font-size:30px; color:#1db9f3">Early History up to Harsha</span><hr>
 <p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left">According  to the Mahabharta the tract which forms the present day Himachal Pradesh was made up of number of small republics known as Janpadas each of which constituted both a state and cultural unit.</p><br>


<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">Audumbras:</span>  They were the most prominent ancient tribes of Himachal who lived in the lower hills between Pathankot and Jwalamukhi. They formed a separate state in 2 B.C.</p><br>


<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">Trigarta:</span>   The state lay in the foothills drained by three rivers, i.e. Ravi, Beas and Satluj and hence the name. It is believed to have been an independent republic.</p><br>


<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">Kuluta:</span>   The kingdom of Kilita was situated in the upper Beas valley which is also known as the Kully valley. Its capital was Naggar.</p><br>


<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">Kulindas:</span>  This kingdom covered the area lying between the Beas, Satluj and Yamuna rivers, i.e. the Shimla and Sirmour hills. Their administration resembled a republic with members of a central assembly sharing the powers of the king.</p><br>


<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">Gupta Empire:</span>  Chandragupta slowly subdued most of the republics of Himachal by show of strength or use of force though he usually did not rule them directly. Ashoka, the grandson of Chandragupta extended his boundaries to the Himalyan region. He introduced Buddhism to this tract. He built many stupas one of which is in the Kullu valley.</p>
<br>
<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">Harsha:</span> After the collapse of Gupta empire and before the rise of  Harsha, this area was again ruled by petty chiefs known as Thakurs and Ranas.  With the rise of Harsha in the early 7th century, most of these small states  acknowledged his overall supremacy though many local powers remained with the  petty chiefs.</p><br>

<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">Rajput Period: </span>  A few decades  after Harsha's death (647 A.D.) many Rajput states ascended in Rajsthan and  Indus plains. They fought amongst themselves and the vanquished moved to the  hills with their followers, where they set up small states or principalities.  These states were Kangra, Nurpur, Suket, Mandi, Kutlehar, Baghal, Bilaspur,  Nalagarh, Keonthal, Dhami, Kunihar, Bushahar, Sirmour.</p><br>

<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">Mughal Rule:</span>
  The small hill kingdom  enjoyed a large degree of independence till the eve of Muslim invasions in  northern India. States of the foothills were devastated by Muslim invaders from  time to time. Mahmud Ghaznavi conquered Kangra at the begining of the 10th  centuary. Timur and Sikander Lodi also marched through the lower hills and  captured several forts and fought many battles. Later on as the  Mughal dynasty began to break up; the rulers of the hill states took full  advantage. The Katoch rulers of Kangra availed of this opportunity and Kangra  regained independence under Maharaja Sansar Chand who ruled for nearly  half a centuary. He was one of the ablest administrators of the region. After  he took formal possession of Kangra fort, Sansar Chand began to expand his  territory. The states of Chamba, Suket, Mandi, Bilaspur, Guler, Jaswan, Siwan  and Datarpur came under the direct or indirect control of Sansar Chand.</p><br>

<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">Anglo-Gorkha and Anglo-Sikh War:</span>
  The Gorkhas, a  martial tribe came to power in Nepal in the year 1768. They consolidated their  military power and began to expand their territory. Gradually the Gorkhas  annexed Sirmour and Shimla hill states. With the leadership of Amar Singh  Thapa, Gorkhas laid siege to Kangra. They managed to defeat Sansar Chand, the  ruler of kangra, in 1806 with the help of many hill chiefs. However Gorkhas  could not capture Kangra fort which came under Maharaja Ranjeet Singh in 1809.  After this defeat the Gorkhas began to expand towards south. This resulted in  the Anglo-Gorkha war. They came into direct conflict with the English along the  tarai belt after which the English expelled them from the hill states east of  the Satluj. Thus British slowly emerged as the paramount powers in this tract. After the  Anglo-Gorkha war the common border of the British domain and Punjab became very  sensitive. Both the Sikh and English wanted to avoid a direct conflict, but  after the death of Ranjit Singh, the Khalsa army fought a number of wars with  the British. In 1845 when the Sikhs invaded the British territory by crossing  the Satluj, the rulers of many hill states sided with the English as they were  looking for an opportunity to settle scores with the former. Many of these  rulers entered into secret communication with the English. After the first  Anglo-Sikh war, the British did not restore the hill territory vacated by Sikhs  to their original owners.</p><br>

<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">Revolt of 1857:</span> The revolt or  first Indian war of independence resulted due to the building up of political,  social, economic, religious and military grievances against the British. People  of the hill states were not politically alive as the people in other parts of  the country. They remained more or less aloof and so did their rulers with the  exception of Bushahr. Some of them even rendered help to the Britishers during the  revolt. Among them were the rulers of Chamba, Bilaspur, Bhagal and Dhami. The  rulers of Bushars acted in a manner hostile to the interests of British.  However it is not clear whether they actually aided the rebels or not.</p><br>

<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">British Rule 1858 to 1914:</span>
  The British  territories in the hill came under British Crown after Queen Victoria's  proclamation of 1858. The states of Chamba, Mandi and Bilaspur made good  progress in many fields during the British rule. During the first World War,  virtually all rulers of the hill states remained loyal and contributed to the  British war effort both in the form of men and materials. Amongst these were  the states of Kangra, Siba, Nurpur, Chamba, Suket, Mandi and Bilaspur.</p><br>

<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:20px; color:#1db9f3">Freedom Struggle 1914 to 1947:</span>
  The people of the  hill also participated in the freedom struggle. Praja Mandal  launched agitations against the British yoke in areas under direct British  Rule. In other princely  states agitations were launched for social and political reforms. However these  were directed more against the princes than against the British and as such  were mere extensions of freedom movement. The Mandi  conspiracy was carried out in 1914-15 under the influence of the Gadhr party.  Meetings were held in Mandi and Suket states in December 1914 and January 1915  and it was decided to murder the Superintendent and Wazir of Mandi and Suket,  to loot the treasury and blow up the bridge over Beas river. However  conspirators were caught and sentenced to long terms in prison. The Pajhota  agitation in which the people of a part of Sirmour state revolted is regarded  as an extension of the Quit India Movement of 1942. Important freedom  fighters of this state during this period included Dr. Y.S. Parmar, Padam Dev,  Shivanand Ramaul, Purnanand, Satya Dev, Sada Ram Chandel, Daulat Ram, Thakur  Hazara Singh and Pahari Gandhi Baba Kanshi Ram.
The Congress party  was also active in the freedom movement in the hill state particularly in  Kangra.<br></p>
<br>



<p style="font-family:'Fengardo Neue'; font-size:18px; font-style:normal; line-height:22px; text-align:left"><span style="font-family:'Fengardo Neue'; font-size:18px; color:#1db9f3">Post-Independence Period:</span> The history of  present day Himachal Pradesh in the post-independence era has been outlined  below:<br>
The Chief  Commissioner's province of H.P. came into being on 15th April, 1948.<br>
H.P. became a part  C state on 26th January, 1950 with the implementation of the Constitution of  India.<br>
Bilaspur was  merged with Himachal Pradesh on 1st July, 1954.<br>
Himachal Pradesh  became Union Territory on 1st November, 1956.<br>
Kangra and most of  the other hill areas of Punjab were merged with H.P. on 1st November, 1966  though its status remained that of a Union Territory.<br>
On 18th December,  1970 the State of Himachal Pradesh Act was passed by Parliament and the new  state came into being on 25th January, 1971. Thus H.P. emerged as the  eighteenth state of Indian Union.<br>
Himachal Pradesh  has come a long way since then. It has seen a number of full-fledged  governments which have led the state towards economic self-reliance.<br>
</p>
